
public class FullTeamException extends Exception {

	public FullTeamException(){
		super();
	}
	
	public FullTeamException(String message){
		super(message);
	}
	
	public FullTeamException(String message, Throwable cause){
		super(message, cause);
	}
	public FullTeamException(Throwable cause){
		super(cause);
	}
}
